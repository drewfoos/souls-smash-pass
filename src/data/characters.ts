// ---------------------------------------------------------------------------
// Seeded, deterministic shuffle (Mulberry32 PRNG + Fisher-Yates)
// Identical seed → identical order, so the deck is stable across refreshes.
// ---------------------------------------------------------------------------

function mulberry32(seed: number): () => number {
  return function () {
    seed |= 0;
    seed = (seed + 0x6d2b79f5) | 0;
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

export function shuffleWithSeed(chars: Character[], seed: number): Character[] {
  const shuffled = [...chars];
  const rand = mulberry32(seed);
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(rand() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

/**
 * Returns a seed that changes every Monday (UTC).
 * All players in the same week see the same deck order.
 * Jan 1 2024 was a Monday → week 0.
 */
export function getWeeklySeed(): number {
  const epochMs = new Date("2024-01-01T00:00:00Z").getTime();
  return Math.floor((Date.now() - epochMs) / (7 * 24 * 60 * 60 * 1000));
}

